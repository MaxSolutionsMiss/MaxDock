create or replace function public.get_location_capacity_projection(
  p_location_id uuid,
  p_at timestamptz,
  p_direction text default 'inbound',
  p_skid_count integer default 0,
  p_exclude_appointment_id uuid default null
)
returns table (
  capacity_enabled boolean,
  enforcement_mode text,
  total_capacity integer,
  reserve_skids integer,
  baseline_occupied integer,
  projected_before integer,
  projected_after integer,
  available_after integer,
  can_accept boolean,
  capacity_message text
)
language plpgsql
stable
security definer
set search_path = ''
as $$
declare
  v_enabled boolean;
  v_mode text;
  v_total integer;
  v_reserve integer;
  v_baseline integer;
  v_as_of timestamptz;
  v_delta integer;
  v_before integer;
  v_after integer;
  v_limit integer;
  v_can_accept boolean;
  v_direction text := lower(trim(coalesce(p_direction, '')));
  v_message text;
begin
  if auth.uid() is null then raise exception 'You must be signed in to view warehouse capacity.'; end if;
  if not public.has_location_access(p_location_id) then
    raise exception 'You do not have access to this MaxDock location.';
  end if;
  if p_at is null then raise exception 'A capacity projection time is required.'; end if;
  if v_direction not in ('inbound', 'outbound') then raise exception 'Direction must be inbound or outbound.'; end if;
  if coalesce(p_skid_count, 0) < 0 then raise exception 'Skid count cannot be negative.'; end if;

  select
    ls.capacity_enabled,
    ls.capacity_enforcement_mode,
    ls.skid_capacity,
    ls.capacity_reserve_skids,
    ls.current_occupied_skids,
    ls.inventory_as_of
  into v_enabled, v_mode, v_total, v_reserve, v_baseline, v_as_of
  from public.location_settings ls
  where ls.location_id = p_location_id and ls.is_active = true;

  if not found then raise exception 'The selected location is not configured.'; end if;

  v_enabled := coalesce(v_enabled, false) and v_total is not null;
  v_mode := coalesce(v_mode, 'warn');
  v_reserve := greatest(coalesce(v_reserve, 0), 0);
  v_baseline := greatest(coalesce(v_baseline, 0), 0);
  v_as_of := coalesce(v_as_of, now());

  select coalesce(sum(
    case lower(coalesce(a.direction, ''))
      when 'inbound' then greatest(coalesce(a.skid_count, 0), 0)
      when 'outbound' then -greatest(coalesce(a.skid_count, 0), 0)
      else 0
    end
  ), 0)::integer
  into v_delta
  from public.appointments a
  where a.location_id = p_location_id
    and a.entry_kind = 'appointment'
    and a.status not in ('cancelled', 'no_show')
    and a.start_at >= v_as_of
    and a.start_at <= p_at
    and (p_exclude_appointment_id is null or a.id <> p_exclude_appointment_id);

  v_before := greatest(v_baseline + coalesce(v_delta, 0), 0);
  v_after := greatest(v_before + case when v_direction = 'inbound' then coalesce(p_skid_count, 0) else -coalesce(p_skid_count, 0) end, 0);
  v_limit := greatest(coalesce(v_total, 0) - v_reserve, 0);
  v_can_accept := not v_enabled or v_direction = 'outbound' or v_after <= v_limit;

  if not v_enabled then
    v_message := 'Warehouse skid-capacity control is not enabled.';
  elsif v_can_accept then
    v_message := format('%s skid spaces remain after this appointment.', greatest(v_limit - v_after, 0));
  else
    v_message := format('Projected occupancy is %s skids; the working limit is %s.', v_after, v_limit);
  end if;

  return query select
    v_enabled, v_mode, v_total, v_reserve, v_baseline,
    v_before, v_after, greatest(v_limit - v_after, 0), v_can_accept, v_message;
end;
$$;

revoke all on function public.get_location_capacity_projection(uuid, timestamptz, text, integer, uuid) from public, anon;
grant execute on function public.get_location_capacity_projection(uuid, timestamptz, text, integer, uuid) to authenticated;

create or replace function public.list_capacity_aware_appointment_slots(
  p_location_id uuid,
  p_date date,
  p_direction text,
  p_appointment_type_code text,
  p_truck_type_code text,
  p_skid_count integer,
  p_handling_type_code text,
  p_is_priority boolean default false,
  p_preferred_start_time time default null,
  p_preferred_end_time time default null,
  p_search_days integer default 7
)
returns table (
  slot_start timestamptz,
  slot_end timestamptz,
  available_docks integer,
  recommendation_rank integer,
  recommendation_score integer,
  recommended_dock_id uuid,
  recommended_dock_name text,
  recommendation_reason text,
  capacity_enabled boolean,
  capacity_warning boolean,
  projected_occupied_skids integer,
  available_skid_capacity integer,
  capacity_message text,
  alternative_date boolean
)
language plpgsql
stable
security definer
set search_path = ''
as $$
declare
  v_timezone text;
  v_direction text := lower(trim(coalesce(p_direction, '')));
begin
  if auth.uid() is null then raise exception 'You must be signed in to view available appointment times.'; end if;
  if v_direction not in ('inbound', 'outbound') then raise exception 'Direction must be inbound or outbound.'; end if;
  if p_date is null then raise exception 'An appointment date is required.'; end if;
  if coalesce(p_search_days, 0) not between 0 and 30 then raise exception 'Capacity search days must be between 0 and 30.'; end if;

  select l.timezone into v_timezone
  from public.locations l
  where l.id = p_location_id and l.is_active = true;
  if not found then raise exception 'The selected location is inactive.'; end if;

  return query
  with candidate_dates as (
    select (p_date + offset_value)::date as candidate_date
    from generate_series(0, least(greatest(coalesce(p_search_days, 0), 0), 30)) offset_value
  ), slots as (
    select dates.candidate_date, slot.*
    from candidate_dates dates
    cross join lateral public.list_smart_appointment_slots(
      p_location_id,
      dates.candidate_date,
      p_appointment_type_code,
      p_truck_type_code,
      p_skid_count,
      p_handling_type_code,
      p_is_priority,
      p_preferred_start_time,
      p_preferred_end_time
    ) slot
  ), assessed as (
    select
      slots.*,
      capacity.capacity_enabled,
      capacity.enforcement_mode,
      capacity.projected_after,
      capacity.available_after,
      capacity.can_accept,
      capacity.capacity_message,
      (slots.slot_start at time zone v_timezone)::date as local_slot_date
    from slots
    cross join lateral public.get_location_capacity_projection(
      p_location_id,
      slots.slot_start,
      v_direction,
      p_skid_count,
      null
    ) capacity
  ), eligible as (
    select assessed.*
    from assessed
    where not assessed.capacity_enabled
       or assessed.enforcement_mode = 'warn'
       or assessed.can_accept
  ), first_available as (
    select min(eligible.local_slot_date) as available_date from eligible
  ), ranked as (
    select
      eligible.*,
      row_number() over (
        order by
          (eligible.recommendation_score - case when eligible.capacity_enabled and not eligible.can_accept then 500 else 0 end) desc,
          eligible.slot_start
      )::integer as final_rank
    from eligible
    cross join first_available
    where eligible.local_slot_date = first_available.available_date
  )
  select
    ranked.slot_start,
    ranked.slot_end,
    ranked.available_docks,
    ranked.final_rank,
    (ranked.recommendation_score - case when ranked.capacity_enabled and not ranked.can_accept then 500 else 0 end)::integer,
    ranked.recommended_dock_id,
    ranked.recommended_dock_name,
    concat_ws(
      ' · ',
      case when ranked.local_slot_date > p_date then format('Next capacity-ready date: %s', to_char(ranked.local_slot_date, 'Mon DD')) end,
      ranked.recommendation_reason,
      case when ranked.capacity_enabled then ranked.capacity_message end
    ),
    ranked.capacity_enabled,
    (ranked.capacity_enabled and not ranked.can_accept),
    ranked.projected_after,
    ranked.available_after,
    ranked.capacity_message,
    (ranked.local_slot_date > p_date)
  from ranked
  order by ranked.slot_start;
end;
$$;

revoke all on function public.list_capacity_aware_appointment_slots(uuid, date, text, text, text, integer, text, boolean, time, time, integer) from public, anon;
grant execute on function public.list_capacity_aware_appointment_slots(uuid, date, text, text, text, integer, text, boolean, time, time, integer) to authenticated;

create or replace function public.book_appointment(
  p_location_id uuid,
  p_date date,
  p_start_time time,
  p_direction text,
  p_requester_type text,
  p_appointment_type_code text,
  p_truck_type_code text,
  p_skid_count integer,
  p_handling_type_code text,
  p_is_priority boolean,
  p_requester_name text,
  p_requester_email text,
  p_external_reference text,
  p_company_name text default null,
  p_requester_location_id uuid default null,
  p_carrier_name text default null,
  p_notes text default null
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_timezone text;
  v_slot_interval integer;
  v_minimum_notice integer;
  v_maximum_advance integer;
  v_is_open boolean;
  v_open_time time;
  v_close_time time;
  v_open_at timestamptz;
  v_close_at timestamptz;
  v_start_at timestamptz;
  v_end_at timestamptz;
  v_duration integer;
  v_local_today date;
  v_dock_id uuid;
  v_dock_name text;
  v_appointment public.appointments%rowtype;
  v_capacity_enabled boolean;
  v_capacity_mode text;
  v_projected_after integer;
  v_available_after integer;
  v_capacity_allowed boolean;
  v_capacity_message text;
begin
  if auth.uid() is null then raise exception 'You must be signed in to book an appointment.'; end if;
  if not public.has_location_access(p_location_id)
     or not public.has_permission('appointment.create') then
    raise exception 'You do not have permission to create appointments for this location.';
  end if;
  if p_date is null or p_start_time is null then raise exception 'Appointment date and start time are required.'; end if;
  if lower(coalesce(p_direction, '')) not in ('inbound', 'outbound') then raise exception 'Direction must be inbound or outbound.'; end if;
  if p_skid_count is null or p_skid_count < 0 then raise exception 'Skid count cannot be negative.'; end if;
  if nullif(trim(coalesce(p_requester_type, '')), '') is null then raise exception 'Requester type is required.'; end if;
  if nullif(trim(coalesce(p_requester_name, '')), '') is null then raise exception 'Requester name is required.'; end if;
  if nullif(trim(coalesce(p_requester_email, '')), '') is null
     or position('@' in p_requester_email) <= 1 then raise exception 'A valid requester email is required.'; end if;
  if nullif(trim(coalesce(p_external_reference, '')), '') is null then raise exception 'PO / BOL / Job number is required.'; end if;

  select l.timezone, ls.slot_interval_minutes, ls.minimum_notice_minutes, ls.maximum_advance_days
  into v_timezone, v_slot_interval, v_minimum_notice, v_maximum_advance
  from public.locations l
  join public.location_settings ls on ls.location_id = l.id
  where l.id = p_location_id and l.is_active and ls.is_active;
  if not found then raise exception 'The selected location is inactive or not configured.'; end if;

  v_local_today := (now() at time zone v_timezone)::date;
  if p_date < v_local_today then raise exception 'Appointments cannot be created in the past.'; end if;
  if p_date > v_local_today + v_maximum_advance then raise exception 'The selected date is beyond this location''s booking window.'; end if;

  select oh.is_open, oh.open_time, oh.close_time
  into v_is_open, v_open_time, v_close_time
  from public.location_operating_hours oh
  where oh.location_id = p_location_id
    and oh.day_of_week = extract(dow from p_date)::smallint;
  if not found or not v_is_open then raise exception 'The selected location is closed on this date.'; end if;

  v_duration := public.calculate_appointment_duration(
    p_location_id, p_appointment_type_code, p_truck_type_code,
    p_skid_count, p_handling_type_code, p_is_priority
  );
  v_open_at := (p_date + v_open_time) at time zone v_timezone;
  v_close_at := (p_date + v_close_time) at time zone v_timezone;
  v_start_at := (p_date + p_start_time) at time zone v_timezone;
  v_end_at := v_start_at + make_interval(mins => v_duration);

  if v_start_at < now() + make_interval(mins => v_minimum_notice) then raise exception 'The selected time does not meet this location''s minimum notice.'; end if;
  if v_start_at < v_open_at or v_end_at > v_close_at then raise exception 'The appointment must fit within the location''s operating hours.'; end if;
  if mod((extract(epoch from (v_start_at - v_open_at)) / 60)::integer, v_slot_interval) <> 0 then
    raise exception 'The selected time is not aligned with the location''s slot interval.';
  end if;

  perform pg_advisory_xact_lock(hashtextextended(p_location_id::text || ':' || p_date::text, 0));

  select
    capacity.capacity_enabled,
    capacity.enforcement_mode,
    capacity.projected_after,
    capacity.available_after,
    capacity.can_accept,
    capacity.capacity_message
  into
    v_capacity_enabled,
    v_capacity_mode,
    v_projected_after,
    v_available_after,
    v_capacity_allowed,
    v_capacity_message
  from public.get_location_capacity_projection(
    p_location_id, v_start_at, lower(p_direction), p_skid_count, null
  ) capacity;

  if v_capacity_enabled and v_capacity_mode = 'enforce' and not v_capacity_allowed then
    raise exception 'This inbound load exceeds the location''s working skid capacity. Choose one of the later suggested times.';
  end if;

  select d.id, d.name
  into v_dock_id, v_dock_name
  from public.docks d
  join public.dock_truck_types dtt
    on dtt.dock_id = d.id
   and dtt.location_id = p_location_id
   and dtt.truck_type_code = p_truck_type_code
  where d.location_id = p_location_id
    and d.is_active
    and not exists (
      select 1 from public.appointments conflict
      where conflict.dock_id = d.id
        and conflict.status <> 'cancelled'
        and conflict.schedule_range && tstzrange(v_start_at, v_end_at, '[)')
    )
  order by
    coalesce((
      select sum(extract(epoch from (day_load.end_at - day_load.start_at)) / 60)
      from public.appointments day_load
      where day_load.dock_id = d.id
        and day_load.status <> 'cancelled'
        and (day_load.start_at at time zone v_timezone)::date = p_date
    ), 0),
    d.sort_order,
    d.name
  limit 1;

  if v_dock_id is null then raise exception 'No compatible dock is available at the selected time.'; end if;

  insert into public.appointments (
    booking_reference, entry_kind, source, location_id, dock_id, start_at, end_at,
    direction, requester_type, requester_location_id, company_name,
    appointment_type_code, truck_type_code, skid_count, handling_type_code,
    is_priority, requester_name, requester_email, carrier_name,
    external_reference, notes, status, created_by, updated_by
  ) values (
    null, 'appointment', 'internal', p_location_id, v_dock_id, v_start_at, v_end_at,
    lower(p_direction), trim(p_requester_type), p_requester_location_id,
    nullif(trim(coalesce(p_company_name, '')), ''), p_appointment_type_code,
    p_truck_type_code, p_skid_count, p_handling_type_code,
    coalesce(p_is_priority, false), trim(p_requester_name),
    lower(trim(p_requester_email)), nullif(trim(coalesce(p_carrier_name, '')), ''),
    trim(p_external_reference), nullif(trim(coalesce(p_notes, '')), ''),
    'scheduled', auth.uid(), auth.uid()
  ) returning * into v_appointment;

  return jsonb_build_object(
    'appointment_id', v_appointment.id,
    'booking_reference', v_appointment.booking_reference,
    'dock_id', v_dock_id,
    'dock_name', v_dock_name,
    'start_at', v_appointment.start_at,
    'end_at', v_appointment.end_at,
    'status', v_appointment.status,
    'capacity_enabled', v_capacity_enabled,
    'projected_occupied_skids', v_projected_after,
    'available_skid_capacity', v_available_after,
    'capacity_message', v_capacity_message
  );
end;
$$;

create or replace function public.update_appointment_details(
  p_appointment_id uuid,
  p_date date,
  p_start_time time,
  p_dock_id uuid,
  p_direction text,
  p_company_name text,
  p_appointment_type_code text,
  p_truck_type_code text,
  p_skid_count integer,
  p_handling_type_code text,
  p_is_priority boolean,
  p_requester_name text,
  p_requester_email text,
  p_carrier_name text,
  p_external_reference text,
  p_notes text
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_appointment public.appointments%rowtype;
  v_timezone text;
  v_slot_interval integer;
  v_open_time time;
  v_close_time time;
  v_is_open boolean;
  v_local_today date;
  v_start_at timestamptz;
  v_end_at timestamptz;
  v_open_at timestamptz;
  v_close_at timestamptz;
  v_duration integer;
  v_capacity_enabled boolean;
  v_capacity_mode text;
  v_capacity_allowed boolean;
begin
  if auth.uid() is null then raise exception 'You must be signed in to edit an appointment.'; end if;
  if public.current_maxdock_role() not in ('system_admin', 'site_admin')
     or not public.has_permission('appointment.update') then
    raise exception 'Only a MaxDock System Admin or Site Admin can edit appointments.';
  end if;

  select * into v_appointment
  from public.appointments a
  where a.id = p_appointment_id
  for update;
  if not found then raise exception 'Appointment not found.'; end if;
  if v_appointment.entry_kind <> 'appointment' then raise exception 'Dock blocks must be managed with the Block Dock Time controls.'; end if;
  if v_appointment.status in ('completed', 'cancelled', 'no_show') then raise exception 'Completed, cancelled, and no-show appointments cannot be edited.'; end if;
  if not public.has_location_access(v_appointment.location_id) then raise exception 'You do not have access to this appointment''s location.'; end if;
  if p_date is null or p_start_time is null or p_dock_id is null then raise exception 'Date, start time, and dock are required.'; end if;
  if lower(trim(coalesce(p_direction, ''))) not in ('inbound', 'outbound') then raise exception 'Direction must be inbound or outbound.'; end if;
  if p_skid_count is null or p_skid_count < 0 then raise exception 'Skid count cannot be negative.'; end if;
  if nullif(trim(coalesce(p_requester_name, '')), '') is null then raise exception 'Requester name is required.'; end if;
  if nullif(trim(coalesce(p_requester_email, '')), '') is null or position('@' in p_requester_email) <= 1 then raise exception 'A valid requester email is required.'; end if;
  if nullif(trim(coalesce(p_external_reference, '')), '') is null then raise exception 'PO / BOL / Job number is required.'; end if;

  select l.timezone, ls.slot_interval_minutes
  into v_timezone, v_slot_interval
  from public.locations l
  join public.location_settings ls on ls.location_id = l.id
  where l.id = v_appointment.location_id and l.is_active = true and ls.is_active = true;
  if not found then raise exception 'The appointment location is inactive or not configured.'; end if;

  v_local_today := (now() at time zone v_timezone)::date;
  if p_date < v_local_today then raise exception 'An active appointment cannot be moved to a past date.'; end if;

  select oh.is_open, oh.open_time, oh.close_time
  into v_is_open, v_open_time, v_close_time
  from public.location_operating_hours oh
  where oh.location_id = v_appointment.location_id
    and oh.day_of_week = extract(dow from p_date)::smallint;
  if not found or not v_is_open then raise exception 'The selected location is closed on this date.'; end if;

  if not exists (
    select 1 from public.docks d
    where d.id = p_dock_id and d.location_id = v_appointment.location_id and d.is_active = true
  ) then raise exception 'The selected dock is invalid or inactive.'; end if;

  v_duration := public.calculate_appointment_duration(
    v_appointment.location_id, p_appointment_type_code, p_truck_type_code,
    p_skid_count, p_handling_type_code, coalesce(p_is_priority, false)
  );
  v_open_at := (p_date + v_open_time) at time zone v_timezone;
  v_close_at := (p_date + v_close_time) at time zone v_timezone;
  v_start_at := (p_date + p_start_time) at time zone v_timezone;
  v_end_at := v_start_at + make_interval(mins => v_duration);

  if v_start_at < v_open_at or v_end_at > v_close_at then raise exception 'The edited appointment must fit within the location''s operating hours.'; end if;
  if mod((extract(epoch from (v_start_at - v_open_at)) / 60)::integer, v_slot_interval) <> 0 then
    raise exception 'The start time is not aligned with the location''s slot interval.';
  end if;

  perform pg_advisory_xact_lock(hashtextextended(v_appointment.location_id::text || ':' || p_date::text, 0));

  select capacity.capacity_enabled, capacity.enforcement_mode, capacity.can_accept
  into v_capacity_enabled, v_capacity_mode, v_capacity_allowed
  from public.get_location_capacity_projection(
    v_appointment.location_id, v_start_at, lower(p_direction), p_skid_count, p_appointment_id
  ) capacity;
  if v_capacity_enabled and v_capacity_mode = 'enforce' and not v_capacity_allowed then
    raise exception 'This edit exceeds the location''s working skid capacity. Choose a later time or reduce the inbound skid count.';
  end if;

  if exists (
    select 1 from public.appointments a
    where a.id <> p_appointment_id
      and a.dock_id = p_dock_id
      and a.status <> 'cancelled'
      and a.schedule_range && tstzrange(v_start_at, v_end_at, '[)')
  ) then raise exception 'The selected dock and time overlap another appointment or dock block.'; end if;

  update public.appointments
  set dock_id = p_dock_id,
      start_at = v_start_at,
      end_at = v_end_at,
      direction = lower(trim(p_direction)),
      company_name = nullif(trim(coalesce(p_company_name, '')), ''),
      appointment_type_code = p_appointment_type_code,
      truck_type_code = p_truck_type_code,
      skid_count = p_skid_count,
      handling_type_code = p_handling_type_code,
      is_priority = coalesce(p_is_priority, false),
      requester_name = trim(p_requester_name),
      requester_email = lower(trim(p_requester_email)),
      carrier_name = nullif(trim(coalesce(p_carrier_name, '')), ''),
      external_reference = trim(p_external_reference),
      notes = nullif(trim(coalesce(p_notes, '')), ''),
      updated_by = auth.uid()
  where id = p_appointment_id
  returning * into v_appointment;

  return jsonb_build_object(
    'appointment_id', v_appointment.id,
    'booking_reference', v_appointment.booking_reference,
    'dock_id', v_appointment.dock_id,
    'start_at', v_appointment.start_at,
    'end_at', v_appointment.end_at,
    'status', v_appointment.status
  );
end;
$$;

