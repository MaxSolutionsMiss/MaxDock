  p_source_name text,
  p_sync_mode text,
  p_daily_sync_time time,
  p_is_enabled boolean,
  p_credential_secret_name text default null
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
begin
  if auth.uid() is null then raise exception 'You must be signed in to save MIS settings.'; end if;
