  ) then raise exception 'Server and database names are required for the secure bridge.'; end if;

  insert into public.mis_integration_settings(
    id, database_type, server_name, server_port, database_name, source_name,
    sync_mode, daily_sync_time, is_enabled, credential_secret_name, updated_by, updated_at
