  return public.admin_get_mis_integration_settings();
end;
$$;

create or replace function public.admin_import_inventory_snapshots(
  p_rows jsonb,
  p_file_name text default null
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_row jsonb;
  v_location public.locations%rowtype;
  v_location_code text;
  v_snapshot_text text;
  v_snapshot_at timestamptz;
  v_occupied integer;
  v_total integer;
  v_reserve integer;
  v_notes text;
  v_count integer := 0;
begin
  if auth.uid() is null then raise exception 'You must be signed in to import MIS data.'; end if;
  if not public.is_system_admin() then raise exception 'Only a MaxDock System Admin can import MIS data.'; end if;
  if p_rows is null or jsonb_typeof(p_rows) <> 'array' then raise exception 'The MIS import must be a JSON row array.'; end if;
  if jsonb_array_length(p_rows) = 0 then raise exception 'The MIS import file is empty.'; end if;
  if jsonb_array_length(p_rows) > 1000 then raise exception 'A maximum of 1,000 inventory rows can be imported at once.'; end if;

  for v_row in select value from jsonb_array_elements(p_rows)
  loop
    v_location_code := upper(trim(coalesce(v_row->>'location_code', '')));
    v_snapshot_text := trim(coalesce(v_row->>'snapshot_at', ''));
    if v_location_code = '' then raise exception 'Every import row requires location_code.'; end if;
    if v_snapshot_text = '' then raise exception 'Every import row requires snapshot_at.'; end if;
    if coalesce(v_row->>'occupied_skids', '') !~ '^[0-9]+$' then raise exception 'occupied_skids must be a whole number.'; end if;

    select * into v_location
    from public.locations l
    where upper(l.code) = v_location_code and l.is_active = true;
    if not found then raise exception 'Unknown or inactive MaxDock location code: %', v_location_code; end if;

    begin
      if v_snapshot_text ~ '(Z|[+-][0-9]{2}:[0-9]{2})$' then
        v_snapshot_at := v_snapshot_text::timestamptz;
      else
        v_snapshot_at := v_snapshot_text::timestamp at time zone v_location.timezone;
      end if;
    exception when others then
      raise exception 'Invalid snapshot_at for location %: %', v_location_code, v_snapshot_text;
    end;

    v_occupied := (v_row->>'occupied_skids')::integer;
    v_total := case when nullif(trim(coalesce(v_row->>'total_skid_capacity', '')), '') is null then null else (v_row->>'total_skid_capacity')::integer end;
    v_reserve := case when nullif(trim(coalesce(v_row->>'reserve_skids', '')), '') is null then null else (v_row->>'reserve_skids')::integer end;
    v_notes := nullif(trim(coalesce(v_row->>'notes', '')), '');
    if v_total is not null and v_total <= 0 then raise exception 'total_skid_capacity must be greater than zero.'; end if;
    if v_reserve is not null and v_reserve < 0 then raise exception 'reserve_skids cannot be negative.'; end if;

    insert into public.location_inventory_snapshots(
      location_id, snapshot_at, occupied_skids, total_skid_capacity, reserve_skids,
      source, source_file_name, notes, imported_by
    ) values (
      v_location.id, v_snapshot_at, v_occupied, v_total, v_reserve,
      'manual_csv', nullif(trim(coalesce(p_file_name, '')), ''), v_notes, auth.uid()
    )
    on conflict (location_id, snapshot_at, source) do update
    set occupied_skids = excluded.occupied_skids,
        total_skid_capacity = excluded.total_skid_capacity,
        reserve_skids = excluded.reserve_skids,
        source_file_name = excluded.source_file_name,
        notes = excluded.notes,
        imported_by = auth.uid(),
        created_at = now();

    update public.location_settings
    set current_occupied_skids = v_occupied,
        inventory_as_of = v_snapshot_at,
        skid_capacity = coalesce(v_total, skid_capacity),
        capacity_reserve_skids = coalesce(v_reserve, capacity_reserve_skids),
        capacity_last_source = 'mis_csv',
        updated_at = now()
    where location_id = v_location.id
      and (inventory_as_of is null or v_snapshot_at >= inventory_as_of);

    v_count := v_count + 1;
  end loop;

  insert into public.mis_import_runs(import_type, file_name, row_count, status, summary, imported_by)
  values (
    'inventory_snapshot', nullif(trim(coalesce(p_file_name, '')), ''), v_count,
    'completed', format('%s warehouse inventory snapshot row%s imported.', v_count, case when v_count = 1 then '' else 's' end), auth.uid()
  );

  update public.mis_integration_settings
  set last_success_at = now(), updated_by = auth.uid(), updated_at = now()
  where id = 1;

  return jsonb_build_object('imported_rows', v_count, 'file_name', p_file_name, 'status', 'completed');
end;
$$;

create or replace function public.admin_list_mis_import_runs()
returns table (
  id bigint,
  import_type text,
  file_name text,
  row_count integer,
  status text,
  summary text,
  imported_by_name text,
  created_at timestamptz
)
language plpgsql
stable
security definer
set search_path = ''
as $$
begin
  if auth.uid() is null then raise exception 'You must be signed in to view MIS imports.'; end if;
  if not public.is_system_admin() then raise exception 'Only a MaxDock System Admin can view MIS imports.'; end if;

  return query
  select
    run.id,
    run.import_type,
    run.file_name,
    run.row_count,
    run.status,
    run.summary,
    coalesce(profile.full_name, profile.username, 'System Admin'),
    run.created_at
  from public.mis_import_runs run
  left join public.profiles profile on profile.id = run.imported_by
  order by run.created_at desc
  limit 20;
end;
$$;

